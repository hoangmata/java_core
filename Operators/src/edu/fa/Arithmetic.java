package edu.fa;

public class Arithmetic {
	public static void main(String[] args) {
		int a = 15;
		int b = 10;
		int c = 12;
		System.out.println("a+b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a % b = " + (a % b));
		System.out.println("a++ = " + a++);
		System.out.println("a-- = " + a--);
		System.out.println("a = " + a);
		System.out.println("++c = " + ++c);
	}

}
